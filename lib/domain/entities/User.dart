class User {
  int _id;
  String _name;
  String _img;
  String _username;

  int get id => _id;

  set id(int value) => _id = value;

  String get name => _name;

  set name(String value) => _name = value;

  String get img => _img;

  set img(String value) => _img = value;

  String get username => _username;

  set username(String value) => _username = value;
}
